#include <stdio.h>
/**
 * main - program that prints the alphabet in lowercase and then in uppercase
 *
 * Return: Always  0
 */
int main(void)
{
	char m;
	char n;

	m = 'a';
	n = 'A';
	while
		(m <= 'z') {
			putchar(m);
			m++;
		}
	while
		(n <= 'z') {
			putchar (n);
			n++;
		}
	putchar('\n');
        return (0);
}
